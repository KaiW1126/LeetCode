# ============================================================
# Valid Parentheses - スタックを用いた括弧の対応チェック
# ============================================================
# 
# 【アルゴリズム】
# - データ構造: Stack (LIFO: Last In First Out)
# - 戦略: 開き括弧をpush、閉じ括弧でpopして対応を検証
#
# 【時間計算量】O(n)
# - n: 文字列の長さ
# - 各文字を1回ずつ走査するため線形時間
#
# 【空間計算量】O(n)
# - 最悪ケース: 全て開き括弧の場合、全てスタックに格納
# - 例: "(((((" → stack size = 5
#
# ============================================================

def is_valid(s)
    stack = []
    
    # 括弧の対応関係をハッシュテーブルで管理
    # Key: 閉じ括弧, Value: 対応する開き括弧
    # ハッシュ探索はO(1)で効率的
    pairs = { ')' => '(', '}' => '{', ']' => '[' }
    
    # 文字列を1文字ずつ走査（O(n)）
    s.each_char do |char|
        # 【ケース1】閉じ括弧の場合
        if pairs.key?(char)
            return false if stack.empty? || stack.pop != pairs[char]
            
        # 【ケース2】開き括弧の場合
        else
            # LIFO原則: 後で対応する閉じ括弧が来たときにpopする
            stack.push(char)
        end
    end
    # 最終チェック: スタックが空 = 全ての括弧が正しく対応
    # スタックに残っている = 閉じられていない開き括弧が存在
    # 例: "(()" → stack: ['('] → false
    stack.empty?
end